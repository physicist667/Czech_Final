export const grammarLessonsSorted = [...grammarLessons].sort((a, b) => {
  const al = levelOrder[a.level] ?? 0;
  const bl = levelOrder[b.level] ?? 0;
  if (al !== bl) return al - bl;
  return a.id.localeCompare(b.id);
});

